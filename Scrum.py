# --- --- --- --- --- USAR METODOLOGIA SCRUM EN GITHUB --- --- --- --- ---

# 1. CREAR UN REPOSITORIO EN GITHUB
# -Ingresar a tu cuenta de Github.
# -Seleccionar el boton verder "New".
# -En el campo "Repository name" ingresar el nombre del repositorio, por ejemplo "Ejercicio-API-Blog".
# -Seleccionar en la opcion "Public" o "Private".
# -Ir a "Initialize this repository with" y seleccionar en el check de "add.gitignore".
#  y tambien seleccionar la opcion ".gitignore template: Python".
# -Ir a "Initialize this repository with" y seleccionar en el check de "Choose a license".
#  y tambien seleccionar la opcion "License: MIT License".
# -Seleccionar el boton verde "Create repository".

# 2. CREAR UN PROYECTO KANBAN
# -Seleccionar la opcion "Projects".
# -Seleccionar el boton verder "Create a project".
# -En el campo "Project board name" ingresar el nombre del proyecto, por ejemplo "Tecsup-API-Blog".
# -En el campo "Project template" seleccionar la opcion "Template: Basic kanban".
# -Seleccionar el boton verde "Create project".
# -Borrar las tarjetas introductorias de la columna "To do".

# 3. PLANIFICAR UN PROYECTO EN KANBAN
# -Para crear una tarea, seleccionar en el icono "+", de la columna "To do", y escribir la descripcion
#  de la tarea y luego seleccionar en el boton verde "Add" para crearla.
# -Para avanzar una tarea, arrastrar la tarea que se quiere ejecutar a la columna "In progress".
# -Para agregar mas informacion a una tarea en ejecucion (responsable, etiqueta, fecha limite) seleccionar 
#  en el icono "..." de cada tarea y luego seleccionar la opcion "convert to issue" la cual mostrara un modal 
#  donde se podra escribir una descripcion y luego seleccionar el boton verde "Convert to issue" para ver los 
#  nuevos campos.

# 4. CONTROLAR UN PROYECTO EN KANBAN
# -Cuando las tareas de la columna "In progress" fueron completadas, arrastrarlas hacia la columna "Done".
# -Para agregar informacion sobre el avance realizado en una tarea, seleccionar en la opcion "Issues", luego 
#  seleccionar en una tarea pendiente e ingresar la informacion.
# -Para finalizar una tarea, seleccionar una tarea y luego seleccionar el boton "Close issue".
